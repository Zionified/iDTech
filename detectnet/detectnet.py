import sys
import argparse

from jetson_inference import detectNet
from jetson_utils import videoSource, videoOutput, Log
from process_image import process_image

# parse the command line
parser = argparse.ArgumentParser(description="Locate objects in a live camera stream using an object detection DNN.", 
                                 formatter_class=argparse.RawTextHelpFormatter, 
                                 epilog=detectNet.Usage() + videoSource.Usage() + videoOutput.Usage() + Log.Usage())

parser.add_argument("input", type=str, default="", nargs='?', help="URI of the input stream")
parser.add_argument("output", type=str, default="", nargs='?', help="URI of the output stream")
parser.add_argument("--network", type=str, default="ssd-mobilenet-v2", help="pre-trained model to load (see below for options)")
parser.add_argument("--overlay", type=str, default="label,box,conf", help="detection overlay flags (e.g. --overlay=box,labels,conf)\nvalid combinations are:  'box', 'labels', 'conf', 'none'")
parser.add_argument("--threshold", type=float, default=0.5, help="minimum detection threshold to use") 

try:
	args = parser.parse_known_args()[0]
except:
	print("")
	parser.print_help()
	sys.exit(0)

# process the image using default detectnet
default_objects = process_image(args.input, args.output, args.network, args.threshold, args.overlay, DEFAULT=True)
print(default_objects)

# if person is detected, immediately give warning
if "person" in default_objects.keys():
    print("Warning!")
else:
    # otherwise, check if fox or bear is present using the trained model
    fox_objects = process_image(args.input, args.output, args.network, args.threshold, args.overlay, DEFAULT=False)

    # if fox or bear is detected and no people are detected, give safe flag
    if fox_objects or "Bear" in default_objects.keys():
        print("Safe!")
    else:
        print("Nothing is detected!")

    print(fox_objects)
    print(default_objects)
